% --- Executes on button press in boton1.
function boton1_Callback(hObject, eventdata, handles)

opt1=get(handles.menu1,'value');
switch(opt1)
    case 1
        delta=0.1;
        A=str2double(get(handles.edit1,'string'));
        ti=str2double(get(handles.edit3,'string'));
        tf=str2double(get(handles.edit7,'string'));
        T=str2double(get(handles.edit2,'string'));
    case 2
        delta=1;
        A=str2double(get(handles.edit6,'string'));
        ti=str2double(get(handles.edit4,'string'));
        tf=str2double(get(handles.edit8,'string'));
        T=str2double(get(handles.edit5,'string'));
end
t=ti:delta:tf;

if opt1==1
    entrada=get(handles.menu2,'value');
switch(entrada)
    case 1
        xt=A.*sin((2*pi.*t)/T);
    case 2
        xt=A.*ones(1,length(t));
    case 3
        xt=A.*trimf(t,[ti,(tf+ti)/2,tf]);
end
    plot(handles.graf1,t,xt,'m')
else
     entrada=get(handles.menu3,'value');
switch(entrada)
    case 1
        xt=A.*sin((2*pi.*t)/T);
    case 2
        xt=A.*ones(1,length(t));
    case 3
        xt=A.*trimf(t,[ti,(tf+ti)/2,tf]);
end
    stem(handles.graf2,t,xt,'m')
end

